// Choose a color for an alien, then write an if-else chain.

// Runs if block
var alien_color = "green";
if (alien_color === "green") {
    console.log("The player just earned 5 points.");
}
else {
    console.log("The player just earned 10 points.");
}
// Runs else block
alien_color = "yellow";
if (alien_color === "green") {
    console.log("The player just earned 5 points.");
}
else {
    console.log("The player just earned 10 points.");
}